# 辅助方法：执行命令，如果失败则显示错误信息
run_or_fail() {
  # $1 是第一个参数（错误说明文字）
  local explanation=$1    # 保存错误说明到局部变量
  
  # shift 1 移除第一个参数
  # 剩下的 $@ 就是要执行的命令及其参数
  shift 1
  
  # 执行命令（$@ 代表所有剩余参数）
  "$@"
  
  # $? 是上一条命令的退出状态码
  # 0 表示成功，非0 表示失败
  if [ $? != 0 ]; then
    # 失败：将错误说明输出到标准错误（stderr）
    echo $explanation 1>&2
    # 退出脚本，返回码 1 表示错误
    exit 1
  fi
}
